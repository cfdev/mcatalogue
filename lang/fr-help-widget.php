<h2>Création du widget</h2>
<h3>Accès à l'administration du widget</h3>
<h4>Accès aux liens bookmarket</h4>
<p>Cliquer sur &quot;<strong>widget</strong>&quot; dans l'admin de pluxml pour accéder à l'administration des widgets..</p>

<p>Cliquer sur l'onglet &quot;<strong>Bookmarket</strong>&quot; et sélectionnez &quot;<strong>bookmarket</strong>&quot; dans la liste déroulante pour accéder à l'administration des liens.</p>
<!--<p><p><img src="../../plugins/spxwbookmarket/lang/img/widget1.jpg" width="50%" height="auto" alt="Gestion des plugins" /></p>-->
<p>Cliquer sur &quot;<strong>nouvelle entrée</strong>&quot; pour créer un nouveau lien.</p>

<p>Sur la nouvelle page qui apparaît, rentrer la date, le titre, l'url et le texte du lien et cliquer sur &quot;<strong>enregistrer</strong>&quot;.</p>
<!--<p><p><img src="../../plugins/spxwbookmarket/lang/img/widget3.jpg" width="50%" height="auto" alt="Gestion des plugins" /></p>-->
<p>Vous venez de créer votre premier lien.</p>
<p>Vous pouvez créer autant de liens que vous désirez. L'ajout se fait donc en appuyant sur &quot;<strong>nouvelle entrée</strong>&quot;.</p>
<p>Pour modifier l'ordre des items, il suffit de cliquer sur l'icone de &quot;<strong>drag and drop</strong>&quot; et de faire un glisser déposer des liens jusqu'à <strong>la position désirée.</strong>.</p>
<!--<p><p><img src="../../plugins/spxwbookmarket/lang/img/widget13.jpg" width="50%" height="auto" alt="Gestion des plugins" /></p>-->
<p>N'oublier pas d'<strong>enregistrer</strong> pour valider la modification de l'ordre de vos liens.</p>
<!--<p><p><img src="../../plugins/spxwbookmarket/lang/img/widget14.jpg" width="50%" height="auto" alt="Gestion des plugins" /></p>-->
<p>A noter :</p>
<ul>
  <li>Pour modifier un lien cliquer sur &quot;<strong>Options</strong>&quot; de la ligne correspondante.</li>
  <li>Pour supprimer un lien, clique dans la case a cocher sur la ligne correspondante, sélectionner &quot;<strong>supprimer</strong>&quot; dans la liste des actions et appuyer sur &quot;<strong>ok</strong>&quot;.</li>
  <li>Pour revenir à liste des menus dans l'édition des menus cliquer sur &quot;<strong>retour à la page précédente</strong>&quot;</li>
  <li>Vous pouvez désactiver un lien en cliquant sur le bouton de désactivation</li>
</ul>
<h4>Accès aux keyword bookmarket</h4>
<p>Cliquer sur l'onglet &quot;<strong>Bookmarket</strong>&quot; et sélectionnez &quot;keyword&quot; dans la liste déroulante pour accéder à l'administration des keyword.</p>
<p>Cliquer sur &quot;<strong>nouvelle entrée</strong>&quot; pour créer un nouveau keyword.</p>
<p>Sur la nouvelle page qui apparaît, rentrer le nom du mot clé et cliquer sur &quot;<strong>enregistrer</strong>&quot;.</p>
<!--<p><p><img src="../../plugins/spxwbookmarket/lang/img/widget3.jpg" width="50%" height="auto" alt="Gestion des plugins" /></p>-->
<p>Vous venez de créer votre premier mot clé.</p>
<p>Vous pouvez créer autant de mots clés que vous désirez. L'ajout se fait donc en appuyant sur &quot;<strong>nouvelle entrée</strong>&quot;.</p>

<h4>Associer des keyword a un lien bookmarket</h4>
<p>Sélectionnez &quot;<strong>bookmarket</strong>&quot; dans la liste déroulante pour accéder à l'administration des liens. Cliquez sur option sur le lien correspondant. Cocher les mots clefs que vous voulez associer puis cliquez sur <strong>&quot;enregistrer</strong>&quot;.</p>


