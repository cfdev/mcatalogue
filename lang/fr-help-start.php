<h2>Pour commencer</h2>
<h3>spxwbookmarket c'est quoi ?</h3>
<p>Ce plugin va vous permettre d'administrer l'affichage de un ou plusieurs liens composés pour chaque lien de :</p>
<ul>
  <li>un titre</li>
  <li>une date</li>
  <li>un liens</li>
  <li>une description</li>
</ul>

<p> l'administration du bookmarket se trouve dans la partie widget :</p>

<p>Un nouveau widget est mis en place : bookmarket.</p>


<h3>Note importante :</h3>
<p>Ce plugin, d'un genre nouveau, est utilisé <strong>comme widget du plugin spxdatas</strong>. Pour rappel : spxdatas permet la gestion d'administration de widgets.</p>
<p>Vous devrez donc avoir installer au préalable le plugin spxdatas dans le dossier plugin de pluxml. Pour la configuration de spxdatas, c'est extrèmement simple... voir l'installation <a target="_blank" href="http://www.secretsitebox.fr/blogspx/article9/spxdatas-configuration-des-donnees">ici</a>.</p>
<p><span class="red" >il est indispensable d'obtenir une version de spxdatas supérieur ou égal à la version 1.60</span>.</p>


<p>Le reste est vraiment très simple. Vous pouvez passer à <a href="parametres_pluginhelp.php?p=spxwbookmarket&page=configuration" target="_self">l'étape suivante</a>.</p>
